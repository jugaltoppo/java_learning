public class Vehicle {
    private int engine;
    private int wheels;
    private int gear;
    private int speed;
    private int door;
    public Vehicle(int engine,int wheels,int gear,int speed, int door)
    {
        this.engine=engine;
        this.wheels=wheels;
        this.gear=gear;
        this.speed=speed;
        this.door=door;
    }
}
