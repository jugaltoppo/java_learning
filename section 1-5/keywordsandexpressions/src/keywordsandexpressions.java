public class keywordsandexpressions
{
    public static void main (String args[])
    {
        double kilometers=(100*1.6093444); // from kilometers to ")" it is a expression
        int highscore=50;// highscore=50 is expression
        if (highscore == 50) // highscore==50 is an expression
        {
            System.out.println("this is an expression");//"this is an expression" is an expression
        }

        // statement
        int myvaraible=90; //this whole line is a statement
        if (myvaraible==90)
        {
            System.out.println("this is also an expression");
        }
        //anything which ends with a semicolon is an expression
    }
}
